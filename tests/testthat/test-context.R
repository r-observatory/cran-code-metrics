test_that("parse_dcf handles simple and multi-line fields", {
  text <- paste0(
    "Package: mypkg\n",
    "Version: 1.2.3\n",
    "Description: A package that does\n",
    "    lots of useful things\n",
    "    across several lines.\n",
    "Imports: dplyr,\n",
    "    ggplot2,\n",
    "    tidyr\n",
    "License: MIT\n"
  )
  d <- parse_dcf(text)

  expect_equal(d[["Package"]], "mypkg")
  expect_equal(d[["Version"]], "1.2.3")
  expect_equal(d[["License"]], "MIT")
  # Multi-line Description is folded (contains "lots of")
  expect_true(grepl("lots of", d[["Description"]]))
  # Multi-line Imports is folded
  expect_true(grepl("ggplot2", d[["Imports"]]))
})

test_that("parse_dcf: last value wins on duplicate fields", {
  text <- "Key: first\nKey: second\n"
  d <- parse_dcf(text)
  expect_equal(d[["Key"]], "second")
})

test_that("parse_dcf returns empty list for blank input", {
  expect_equal(parse_dcf(""),      list())
  expect_equal(parse_dcf("   \n"), list())
  expect_equal(parse_dcf(NULL),    list())
})

